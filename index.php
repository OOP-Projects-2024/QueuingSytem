Queuing Monitoring System

Overview
The Queuing Monitoring System is a web-based application designed to track 
and manage queues in various settings. Developed using PHP and following camel 
casing conventions for variable naming, this system enables administrators to 
monitor and control the flow of people or tasks in a queue in real-time. 
It helps streamline operations by providing a visual overview of queue status, 
allowing quick adjustments and management.

